# Random Module
import random

# Python List - A collection of items
my_list = [1, 2, 3, 4, 5]
random_choice = random.choice(my_list)  # Picks a random item from the list

# Functions with Return Statements
def add(a, b):
    return a + b

result = add(3, 5)  # result = 8

# Docstrings - Used to document what a function does
def multiply(x, y):
    """This function multiplies two numbers."""
    return x * y

# Slicing Lists - Extracting parts of a list
numbers = [10, 20, 30, 40, 50]
print(numbers[1:4])  # Output: [20, 30, 40]

